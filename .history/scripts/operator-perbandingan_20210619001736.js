/*
    > => Lebih Dari
    < => Kurang Dari
    >= => Lebih Dari sama dengan
    <= => Kurang dari sama dengan
    == => Sama Dengan
    === => Sama dengan dan sama tipe
    != => Tidak sama dengan
    !== Tidak sama dengan atau tidak sama tipe
*/

let result = 5 == "5";
document.writeln("<p>" +result+ "</p>");

result = 5 === "5";
document.writeln("<p>" +result+ "</p>");

result = 5 > "10";
document.writeln("<p>" +result+ "</p>");