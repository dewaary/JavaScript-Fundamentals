/*
OPERATOR ARITMATIKA

    + = Pertamabahan
    - = Pengurangan
    * = Perkalian
    ** = Exponsial (Pemangkatan)
    / = Pembagian
    % = Modulus
*/

let result = 1 + 2;
document.writeln("<p> 1 + 2 = " + result + "</p>");
let orginalResult = result;


result = result - 1;
document.writeln("<p>" + orginalResult + " -1 = " +result+ "</p>")
orginalResult = result;

result = result * 2;
document.writeln("<p>" + orginalResult + " * 4 = " +result+ "</p>")